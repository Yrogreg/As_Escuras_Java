    private void bt_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_loginActionPerformed
        try {
            // Check if the text fields are not null or empty
            if (txt_login.getText().isEmpty() || txt_senha.getPassword().length == 0) {
                JOptionPane.showMessageDialog(null, "Campos de login ou senha não podem ser nulos.");
                return;
            }
            
            User_login user = new User_login();
            boolean authenticated = user.login(txt_login.getText(), new String(txt_senha.getPassword()));
            
            if (authenticated) {
                System.out.println("usuario autenticado");
                
                F_Home h = new F_Home();
                this.dispose();
                h.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Login ou senha inválidos.");
            }
        } catch (HeadlessException e) {
            System.out.println("Erro inesperado: " + e.getMessage());
        } catch (Exception e) {
            System.out.println("Erro inesperado: " + e.getMessage());
        }
    }//GEN-LAST:event_bt_loginActionPerformed

